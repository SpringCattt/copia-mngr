class Evento{
    constructor(id, tipo_evento, titulo, data_hora, loca, descricao, oficial, id_equipa, criado_por, estado){    
        this.id = id;
        this.tipo_evento = tipo_evento;
        this.titulo = titulo;
        this.data_hora = data_hora;
        this.loca = loca;
        this.descricao = descricao;
        this.oficial = oficial;
        this.id_equipa = id_equipa;
        this.criado_por = criado_por;
        this.estado = estado;
    }
}